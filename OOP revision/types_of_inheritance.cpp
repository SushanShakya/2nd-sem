/*
C++ supports five types of inheritance:

Single inheritance
Multiple inheritance
Hierarchical inheritance
Multilevel inheritance
Hybrid inheritance
*/

/*
Single inheritnce
Single inheritance means a derived class inherits all the members of a base class.

*/

/* 
Multiple inheritance
Multiple inheritance is a type of inheritance where a derived class inherits the members of more than one base class.

*/


/*
Hierarchical inheritance
Hierarchical inheritance is a type of inheritance where a derived class inherits the members of one or more base classes.
*/